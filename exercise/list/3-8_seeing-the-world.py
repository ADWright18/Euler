# Store and print list of locations

locations = ['Boston','New York','Baltimore','Tokyo','Beijing']
print(locations)

# Print sorted list
print(sorted(locations))

# Show that the list is in original order
print(locations)

# Print the list in reverse order with sorted()
print(sorted(locations, reverse=True))

# Reverse and print list with reverse()
locations.reverse()
print(locations)

# Reverse the list again and Print
locations.reverse()
print(locations)

# Use sort() on list and print
locations.sort()
print(locations)

# Use sort() to reverse the order
locations.sort(reverse=True)
print(locations)
